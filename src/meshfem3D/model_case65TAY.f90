  subroutine model_case65TAY_broadcast(CRUSTAL)

! standard routine to setup model

  use constants, only: myrank
  use model_case65tay_par

  implicit none

  logical :: CRUSTAL

  ! local parameters
  integer :: ier

  ! allocates model arrays
  allocate(Mcase65TAY_V_radius(NR_case65TAY), &
           Mcase65TAY_V_density(NR_case65TAY), &
           Mcase65TAY_V_vp(NR_case65TAY), &
           Mcase65TAY_V_vs(NR_case65TAY), &
           Mcase65TAY_V_Qkappa(NR_case65TAY), &
           Mcase65TAY_V_Qmu(NR_case65TAY), &
           stat=ier)
  if (ier /= 0 ) call exit_MPI(myrank,'Error allocating Mcase65TAY_V arrays')
  Mcase65TAY_V_radius(:) = 0.d0
  Mcase65TAY_V_density(:) = 0.d0
  Mcase65TAY_V_vp(:) = 0.d0
  Mcase65TAY_V_vs(:) = 0.d0
  Mcase65TAY_V_Qkappa(:) = 9999.d0
  Mcase65TAY_V_Qmu(:) = 9999.d0

  ! all processes will define same parameters
  call define_model_case65TAY(CRUSTAL)

  end subroutine model_case65TAY_broadcast

