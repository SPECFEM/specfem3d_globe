  subroutine model_case65TAY(x,rho,vp,vs,Qkappa,Qmu,iregion_code)

  use constants
  use shared_parameters, only: R_PLANET,RHOAV

  use model_case65tay_par

  implicit none

! input:
! radius r: meters

! output:
! density rho: kg/m^3
! compressional wave speed vp: km/s
! shear wave speed vs: km/s

  double precision :: x,rho,vp,vs,Qmu,Qkappa
  integer :: iregion_code

  ! local parameters
  double precision :: r,frac,scaleval
  integer :: i

  ! compute real physical radius in meters
  r = x * R_PLANET

  i = 1
  do while(r >= Mcase65TAY_V_radius(i) .and. i /= NR_case65TAY)
    i = i + 1
  enddo

  ! make sure we stay in the right region and never take a point above
  ! and a point below the ICB or the CMB and interpolate between them,
  ! which would lead to a wrong value (keeping in mind that we interpolate
  ! between points i-1 and i below)

  ! see table entry below
  ! inner core range [1:104]
  if (iregion_code == IREGION_INNER_CORE .and. i > 104) i = 104

  ! outer core index range [105:370]
  ! due to the interpolation below, we add +1 to the bottom
  if (iregion_code == IREGION_OUTER_CORE .and. i <= 105) i = 105 + 1
  if (iregion_code == IREGION_OUTER_CORE .and. i > 370) i = 370

  ! crust/mantle index range [371:681]
  ! due to the interpolation below, we add +1 to the bottom
  if (iregion_code == IREGION_CRUST_MANTLE .and. i <= 371) i = 371 + 1
  if (iregion_code == IREGION_CRUST_MANTLE .and. i > NR_case65TAY) i = NR_case65TAY

  ! model value interpolation
  if (i == 1) then
    rho = Mcase65TAY_V_density(i)
    vp = Mcase65TAY_V_vp(i)
    vs = Mcase65TAY_V_vs(i)
    Qmu = Mcase65TAY_V_Qmu(i)
    Qkappa = Mcase65TAY_V_Qkappa(i)
  else
    ! interpolate from radius(i-1) to r using the values at i-1 and i
    frac = (r-Mcase65TAY_V_radius(i-1))/(Mcase65TAY_V_radius(i)-Mcase65TAY_V_radius(i-1))

    rho = Mcase65TAY_V_density(i-1) + frac * (Mcase65TAY_V_density(i)-Mcase65TAY_V_density(i-1))
    vp = Mcase65TAY_V_vp(i-1) + frac * (Mcase65TAY_V_vp(i)-Mcase65TAY_V_vp(i-1))
    vs = Mcase65TAY_V_vs(i-1) + frac * (Mcase65TAY_V_vs(i)-Mcase65TAY_V_vs(i-1))
    Qmu = Mcase65TAY_V_Qmu(i-1) + frac * (Mcase65TAY_V_Qmu(i)-Mcase65TAY_V_Qmu(i-1))
    Qkappa = Mcase65TAY_V_Qkappa(i-1) + frac * (Mcase65TAY_V_Qkappa(i)-Mcase65TAY_V_Qkappa(i-1))
  endif

  ! make sure Vs is zero in the outer core even if roundoff errors on depth
  ! also set fictitious attenuation to a very high value (attenuation is not used in the fluid)
  if (iregion_code == IREGION_OUTER_CORE) then
    vs = 0.d0
    Qkappa = 3000.d0
    Qmu = 3000.d0
  endif

  ! non-dimensionalize
  ! time scaling (s^{-1}) is done with scaleval
  scaleval = dsqrt(PI*GRAV*RHOAV)
  rho = rho*1000.0d0/RHOAV
  vp = vp*1000.0d0/(R_PLANET*scaleval)
  vs = vs*1000.0d0/(R_PLANET*scaleval)

  end subroutine model_case65TAY

