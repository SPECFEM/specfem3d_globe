!--------------------------------------------------------------------------------------------------
!
! 1D REF model of Kustowski et al. (2008)
!
! this is STW105 - new reference model, also known as REF
!
! A recent 1D Earth model developed by Kustowski et al. This model is the 1D background
! model for the 3D models s362ani, s362wmani, s362ani_prem, and s29ea.
!
! see chapter 3, in:
! Kustowski, B, Ekstr"om, G., and A. M. Dziewonski, 2008,
! Anisotropic shear-wave velocity structure of the Earth's mantle: A global model,
! J. Geophys. Res., 113, B06306, doi:10.1029/2007JB005169.
!
! model is identical to PREM at crustal depths, between 220 and 400km
! and below 670km.
!
! attenuation structure is taken from model QL6:
! Durek, J. J. and G. Ekstr"om, 1996.
! A radial model of anelasticity consistent with long period surface wave attenuation,
! Bull. Seism. Soc. Am., 86, 144-158
!
! 1-D profiles: see http://www.iris.edu/ds/products/emc-stw105/
!               internal discontinuities: contains a 410-km and 650-km discontinuity
!--------------------------------------------------------------------------------------------------

  module model_1dref_par

  ! number of layers in DATA/s362ani/REF
  integer, parameter :: NR_REF = 750

  ! model_1dref_variables
  double precision, dimension(:),allocatable :: Mref_V_radius_ref,Mref_V_density_ref, &
                                                Mref_V_vpv_ref,Mref_V_vph_ref, &
                                                Mref_V_vsv_ref,Mref_V_vsh_ref, &
                                                Mref_V_eta_ref,Mref_V_Qkappa_ref,Mref_V_Qmu_ref

  end module model_1dref_par

!
!-------------------------------------------------------------------------------------------------
!

  subroutine model_1dref_broadcast(CRUSTAL)

! standard routine to setup model

  use model_1dref_par

  implicit none

  logical :: CRUSTAL

  ! local parameters
  integer :: ier

  ! allocates arrays
  allocate(Mref_V_radius_ref(NR_REF), &
           Mref_V_density_ref(NR_REF), &
           Mref_V_vpv_ref(NR_REF), &
           Mref_V_vph_ref(NR_REF), &
           Mref_V_vsv_ref(NR_REF), &
           Mref_V_vsh_ref(NR_REF), &
           Mref_V_eta_ref(NR_REF), &
           Mref_V_Qkappa_ref(NR_REF), &
           Mref_V_Qmu_ref(NR_REF), &
           stat=ier)
  if (ier /= 0 ) stop 'Error allocating 1d_ref arrays'

  ! all processes will define same parameters
  call define_model_1dref(CRUSTAL)

  end subroutine model_1dref_broadcast

!
!-------------------------------------------------------------------------------------------------
!

  subroutine model_1dref(x,rho,vpv,vph,vsv,vsh,eta,Qkappa,Qmu,iregion_code,CRUSTAL)

  use constants
  use model_1dref_par

  implicit none

! model_1dref_variables

! input:
! dimensionless radius x

! output: non-dimensionalized
! mass density rho
! compressional wave speed vpv
! compressional wave speed vph
! shear wave speed vsv
! shear wave speed vsh
! dimensionless parameter eta
! shear quality factor Qmu
! bulk quality factor Qkappa

  double precision :: x,rho,vpv,vph,vsv,vsh,eta,Qmu,Qkappa
  integer :: iregion_code
  logical :: CRUSTAL

  ! local parameters
  double precision :: r,frac,scaleval
  integer :: i

  ! compute real physical radius in meters
  r = x * R_EARTH

  i = 1
  do while(r >= Mref_V_radius_ref(i) .and. i /= NR_REF)
    i = i + 1
  enddo

! make sure we stay in the right region
  if (iregion_code == IREGION_INNER_CORE .and. i > 180) i = 180

  if (iregion_code == IREGION_OUTER_CORE .and. i < 182) i = 182
  if (iregion_code == IREGION_OUTER_CORE .and. i > 358) i = 358

  if (iregion_code == IREGION_CRUST_MANTLE .and. i < 360) i = 360

  ! if crustal model is used, mantle gets expanded up to surface
  ! for any depth less than 24.4 km, values from mantle below moho are taken
  if (CRUSTAL .and. i > 717) i = 717


  if (i == 1) then
    ! first layer in inner core
    rho = Mref_V_density_ref(i)
    vpv = Mref_V_vpv_ref(i)
    vph = Mref_V_vph_ref(i)
    vsv = Mref_V_vsv_ref(i)
    vsh = Mref_V_vsh_ref(i)
    eta = Mref_V_eta_ref(i)
    Qkappa = Mref_V_Qkappa_ref(i)
    Qmu = Mref_V_Qmu_ref(i)
  else
    ! interpolates between one layer below to actual radius layer,
    ! that is from radius_ref(i-1) to r using the values at i-1 and i
    frac = (r-Mref_V_radius_ref(i-1))/(Mref_V_radius_ref(i)-Mref_V_radius_ref(i-1))
    ! interpolated model parameters
    rho = Mref_V_density_ref(i-1) + frac * (Mref_V_density_ref(i)-Mref_V_density_ref(i-1))
    vpv = Mref_V_vpv_ref(i-1) + frac * (Mref_V_vpv_ref(i)-Mref_V_vpv_ref(i-1))
    vph = Mref_V_vph_ref(i-1) + frac * (Mref_V_vph_ref(i)-Mref_V_vph_ref(i-1))
    vsv = Mref_V_vsv_ref(i-1) + frac * (Mref_V_vsv_ref(i)-Mref_V_vsv_ref(i-1))
    vsh = Mref_V_vsh_ref(i-1) + frac * (Mref_V_vsh_ref(i)-Mref_V_vsh_ref(i-1))
    eta = Mref_V_eta_ref(i-1) + frac * (Mref_V_eta_ref(i)-Mref_V_eta_ref(i-1))
    Qkappa = Mref_V_Qkappa_ref(i-1) + frac * (Mref_V_Qkappa_ref(i)-Mref_V_Qkappa_ref(i-1))
    Qmu = Mref_V_Qmu_ref(i-1) + frac * (Mref_V_Qmu_ref(i)-Mref_V_Qmu_ref(i-1))
  endif

  ! make sure Vs is zero in the outer core even if roundoff errors on depth
  ! also set fictitious attenuation to a very high value (attenuation is not used in the fluid)
  if (iregion_code == IREGION_OUTER_CORE) then
    vsv = 0.d0
    vsh = 0.d0
    Qkappa = 3000.d0
    Qmu = 3000.d0
  endif

  ! non-dimensionalize
  ! time scaling (s^{-1}) is done with scaleval
  scaleval = dsqrt(PI*GRAV*RHOAV)
  rho = rho/RHOAV
  vpv = vpv/(R_EARTH*scaleval)
  vph = vph/(R_EARTH*scaleval)
  vsv = vsv/(R_EARTH*scaleval)
  vsh = vsh/(R_EARTH*scaleval)

  end subroutine model_1dref

